## arithmetic operators

num_1 <- 32
num_2 <- 5

print("*********** Numbers ***********")
print(num_1)
print(num_2)

print("Addition")
print(num_1 + num_2)
print("Subtraction")
print(num_1 - num_2)
print("Multiplication")
print(num_1 * num_2)
print("Division")
print(num_1 / num_2)
print("Exponentiation")
print(num_1 ^ num_2)
print("Modulus")
print(num_1 %% num_2)
print("Integer Division")
print(num_1 %/% num_2)

